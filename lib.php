/**
 * Calls the Perfex CRM API with a given endpoint and token.
 *
 * @param string $endpoint The API endpoint (e.g., 'customers', 'projects').
 * @param string $token Your API authentication token.
 * @return array The decoded JSON response from the API.
 */
function local_crm_integration_call_api($endpoint, $token) {
    $crm_base_url = 'https://support.paktaleem.com';
    $url = $crm_base_url . '/api/' . $endpoint;
    $ch = curl_init();
    curl_setopt($ch, CURLOPT_URL, $url);
    curl_setopt($ch, CURLOPT_RETURNTRANSFER, 1);
    curl_setopt($ch, CURLOPT_CUSTOMREQUEST, 'GET');
    curl_setopt($ch, CURLOPT_SSL_VERIFYPEER, false);
    curl_setopt($ch, CURLOPT_SSL_VERIFYHOST, false);
    $headers = ['authtoken: ' . $token];
    curl_setopt($ch, CURLOPT_HTTPHEADER, $headers);
    $response = curl_exec($ch);
    curl_close($ch);
    return json_decode($response, true);
}